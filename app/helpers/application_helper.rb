# Methods added to this helper will be available to all templates in the application.
module ApplicationHelper
  def liquid(app)
    Liquid::Template.parse(app.theme.template).render 'app' => @app,
      'analytics' => google_analytics(app.google_analytics_id),
      'footer' => template_footer
  end
  
  def template_footer
    render :partial => "themes/footer"
  end
  
  def google_analytics(google_analytics_id)
    if google_analytics_id
      <<-EOF
      <script type="text/javascript">
        var gaJsHost = (("https:" == document.location.protocol) ? "https://ssl." : "http://www.");
        document.write(unescape("%3Cscript src=’" + gaJsHost + "google-analytics.com/ga.js’ type=’text/javascript’%3E%3C/script%3E"));
      </script>
      <script type="text/javascript">
        try {
          var pageTracker = _gat._getTracker("#{google_analytics_id}");
    			pageTracker._trackPageview();
          //var firstTracker = _gat._getTracker("#{google_analytics_id}");
          //firstTracker._initData();
          //firstTracker._trackPageview();
          //var secondTracker = _gat._getTracker("UA-13182964-1");
          //secondTracker._setDomainName("none");
          //secondTracker._setAllowLinker(true);
          //secondTracker._initData();
          //secondTracker._trackPageview();
        } catch(err) {}
      </script>
      EOF
    end
  end
  
  def strip_app_id(url)
    url.scan(/id(\d+)/).first.first
  end
  
  def clippy(text, bgcolor='#FFFFFF')
    html = <<-EOF
      <object classid="clsid:d27cdb6e-ae6d-11cf-96b8-444553540000"
              width="110"
              height="14"
              id="clippy" >
      <param name="movie" value="/flash/clippy.swf"/>
      <param name="allowScriptAccess" value="always" />
      <param name="quality" value="high" />
      <param name="scale" value="noscale" />
      <param NAME="FlashVars" value="text=#{text}">
      <param name="bgcolor" value="#{bgcolor}">
      <embed src="/flash/clippy.swf"
             width="110"
             height="14"
             name="clippy"
             quality="high"
             allowScriptAccess="always"
             type="application/x-shockwave-flash"
             pluginspage="http://www.macromedia.com/go/getflashplayer"
             FlashVars="text=#{text}"
             bgcolor="#{bgcolor}"
      />
      </object>
    EOF
  end
end
